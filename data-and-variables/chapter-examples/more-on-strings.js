console.log(typeof "17");
//this statement prints out the type of the value, "17" using typeof and prints out string because the value is enclosed in double quotes, meaning it is a string.
console.log(typeof "3.2");
//the output is once again string because the value is enclosed in double quotes, making it a string
console.log(typeof 'This is a string');
//this prints out string as well because the value is enclosed in single quotes.
console.log(typeof "And so is this");
//this is also a string 